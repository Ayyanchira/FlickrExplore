struct PhotoInfo: Codable {
    let photo: PhotoDetail
    let stat: String
    
    enum CodingKeys: String, CodingKey {
        case photo = "photo"
        case stat = "stat"
    }
}


struct PhotoDetail: Codable {
    let id, secret, server: String
    let farm: Int
    let dateuploaded: String
    let isfavorite: Int
    let license, safetyLevel: String
    let rotation: Int
    let originalsecret, originalformat: String
    let owner: Owner
    let title, description: Comments
    let visibility: Visibility
    let dates: Dates
    let views: String
    let editability, publiceditability: Editability
    let usage: Usage
    let comments: Comments
    let notes: Notes
    let people: People
    let tags: Tags
    let urls: Urls
    let media: String
    
    enum CodingKeys: String, CodingKey {
        case id, secret, server, farm, dateuploaded, isfavorite, license
        case safetyLevel = "safety_level"
        case rotation, originalsecret, originalformat, owner, title, description, visibility, dates, views, editability, publiceditability, usage, comments, notes, people, tags, urls, media
    }
}

struct Comments: Codable {
    let content: String
    
    enum CodingKeys: String, CodingKey {
        case content = "_content"
    }
}

struct Dates: Codable {
    let posted, taken: String
    let takengranularity: Int
    let takenunknown, lastupdate: String
}

struct Editability: Codable {
    let cancomment, canaddmeta: Int
}

struct Notes: Codable {
    let note: [JSONAny]
}

struct Owner: Codable {
    let nsid: String
    let username, realname: String
    let location, iconserver: String
    let iconfarm: Int
    let pathAlias: String
    
    enum CodingKeys: String, CodingKey {
        case nsid, username, realname, location, iconserver, iconfarm
        case pathAlias = "path_alias"
    }
}

struct People: Codable {
    let haspeople: Int
}

struct Tags: Codable {
    let tag: [Tag]
}

struct Tag: Codable {
    let id: String
    let author: String
    let authorname: String
    let raw, content: String
    let machineTag: Bool
    
    enum CodingKeys: String, CodingKey {
        case id, author, authorname, raw
        case content = "_content"
        case machineTag = "machine_tag"
    }
}

struct Urls: Codable {
    let url: [URLElement]
}

struct URLElement: Codable {
    let type: String
